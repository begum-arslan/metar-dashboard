// Quick lookup maps for globe airports
const byICAO = {};
const byIATA = {};
AIRPORTS.forEach(a => {
  if (a.icao) byICAO[a.icao] = a;
  if (a.iata) byIATA[a.iata] = a;
});

// Full database lookup maps
const dbByICAO = {};
const dbByIATA = {};
for (const icao in allAirports) {
  const [iata, name] = allAirports[icao];
  const obj = { icao, iata, name };
  dbByICAO[icao] = obj;
  if (iata) dbByIATA[iata] = obj;
}

/**
 * Find an airport by either IATA (3 chars) or ICAO (4 chars) code
 * @returns {{ airport: object|null, icao: string }} 
 */
export function findAirport(code) {
  const upper = code.toUpperCase().trim();
  
  if (upper.length === 3) {
    // Try to find in globe first, then global DB
    let airport = byIATA[upper] || dbByIATA[upper] || null;
    return { airport, icao: airport ? airport.icao : null };
  }
  
  if (upper.length === 4) {
    let airport = byICAO[upper] || dbByICAO[upper] || null;
    return { airport, icao: upper };
  }
  
  return { airport: null, icao: upper };
}

export function getAllAirports() {
  return AIRPORTS;
}

